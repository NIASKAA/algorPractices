// Write code to return a new string with all of the characters in `str` reversed.
// You may NOT use the built-in reverse method

const reverseInPlace = (arr) => {
    let newString = ""

    for (var i = arr.length - 1; i => 0; i--) {
        newString += arr[i];
        return newString;
    }
};

reverseInPlace("str")